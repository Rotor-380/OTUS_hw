def amulet_area(a, b, c):
    p = (a + b + c) / 2
    S = int((p * (p - a) * (p - b) * (p - c)) ** 0.5)
    return S


assert amulet_area(3, 4, 5) == 6
print(amulet_area(3, 4, 5))

