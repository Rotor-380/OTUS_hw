def cal_euclidean(a, b):    # –ï–≤–∫–ª–∏–¥–æ–≤–æ —Ä–∞—Å—Å—Ç–æ—è–Ω–∏–µ (–±–∞–∑–æ–≤—ã–µ —Ñ—É–Ω–∫—Ü–∏–∏ numpy)

    distance = (np.sum((a-b)**2))**0.5
    return distance


def cal_manhattan(a, b):  # –†–∞—Å—Å—Ç–æ—è–Ω–∏–µ –ú–∞–Ω—Ö—ç—Ç—Ç–µ–Ω–∞ (–±–∞–∑–æ–≤—ã–µ —Ñ—É–Ω–∫—Ü–∏–∏ numpy)
    distance = np.sum(np.abs(a-b))
    return distance
#
#
def cal_cosine(a, b):   # –ö–æ—Å–∏–Ω—É—Å–Ω–æ–µ —Ä–∞—Å—Å—Ç–æ—è–Ω–∏–µ (–±–∞–∑–æ–≤—ã–µ —Ñ—É–Ω–∫—Ü–∏–∏ numpy)
    distance = 1 - ( (np.dot(a, b))/(norm(a) * norm(b)) )
    return distance


a = np.random.randint(-10, 10, size=10)
b = np.random.randint(-10, 10, size=10)
print(cal_euclidean(a, b))
print(cal_manhattan(a, b))
print(cal_cosine(a, b))

